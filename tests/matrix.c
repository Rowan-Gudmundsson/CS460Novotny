int main() {
	int a[2][2];
	int b[2][2];
	int c[2][2];
	int i, j, k;

	// a = ( 7 3 )
	//     ( 5 1 )
	a[0][0] = 7;
	a[0][1] = 3;
	a[1][0] = 5;
	a[1][1] = 1;

	// b = ( 2 6 )
	//     ( 9 2 )
	b[0][0] = 2;
	b[0][1] = 6;
	b[1][0] = 9;
	b[1][1] = 2;

	// c = a b
	for (i = 0; i < 2; i++) {
		for (j = 0; j < 2; j++) { c[i][j] = 0; }
	}
	for (i = 0; i < 2; i++) {
		for (j = 0; j < 2; j++) {
			for (k = 0; k < 2; k++) { c[i][j] += a[i][k] * b[k][j]; }
		}
	}
}
